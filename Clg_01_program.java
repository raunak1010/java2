public class Clg_01_program {
    public static void main(String[]args){
        //data type
        byte a = 125;
        short b= a;
        System.out.println(b);

        int c = 334534;
        short d = (short)c;
        System.out.println(d);


        short e = 18397;
        long f = e;
        System.out.println(f);

        float g = 92783F;
        long h = (long)g;
        System.out.println(h);


        int i = 7;
        char j = (char)i;
        System.out.println(j);

        char k = 'a';
        int l = k;
        System.out.println(l);

        int m = 90;
        float n = m;
        System.out.println(n);


}}
